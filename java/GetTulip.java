// Minimal natural cubic spline interpolator (no external dependencies).
static class CubicSpline {
    private final double[] x, a, b, c, d;
    private final int n;

    public CubicSpline(double[] x, double[] y) {
        this.x = x;
        n = x.length - 1;
        a = new double[n]; b = new double[n]; c = new double[n + 1]; d = new double[n];

        double[] h = new double[n];
        double[] alpha = new double[n];
        for (int i = 0; i < n; i++) h[i] = x[i + 1] - x[i];
        for (int i = 1; i < n; i++)
            alpha[i] = (3 / h[i]) * (y[i + 1] - y[i]) - (3 / h[i - 1]) * (y[i] - y[i - 1]);

        double[] l = new double[n + 1];
        double[] mu = new double[n + 1];
        double[] z = new double[n + 1];
        l[0] = 1; mu[0] = 0; z[0] = 0;

        for (int i = 1; i < n; i++) {
            l[i] = 2 * (x[i + 1] - x[i - 1]) - h[i - 1] * mu[i - 1];
            mu[i] = h[i] / l[i];
            z[i] = (alpha[i] - h[i - 1] * z[i - 1]) / l[i];
        }

        l[n] = 1; z[n] = 0; c[n] = 0;
        for (int j = n - 1; j >= 0; j--) {
            c[j] = z[j] - mu[j] * c[j + 1];
            b[j] = (y[j + 1] - y[j]) / h[j] - h[j] * (c[j + 1] + 2 * c[j]) / 3;
            d[j] = (c[j + 1] - c[j]) / (3 * h[j]);
            a[j] = y[j];
        }
    }

    public double value(double xq) {
        int i = 0;
        while (i < n - 1 && xq > x[i + 1]) i++;
        double dx = xq - x[i];
        return a[i] + b[i] * dx + c[i] * dx * dx + d[i] * dx * dx * dx;
    }
}



