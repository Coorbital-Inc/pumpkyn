    /**
     * Interpolates x, z, ydot for a given tau0 and Np family.
     *
     * @param tau0 single user-supplied period value
     * @param Np      tulip family index (1,2,3,…)
     * @param pm      +1 = northern, −1 = southern
     * @return        [tau0, x, y, z, xdot, ydot, zdot, muStar, tStar, lStar]
     */
    public static double[] getTulip(double tau0, int Np, int pm) {
    // ---- Select family table ----
    double[][] family;
    switch (Np) {
        case 1:  family = tau_x0_N1;  break;
        case 2:  family = tau_x0_N2;  break;
        case 3:  family = tau_x0_N3;  break;
        case 4:  family = tau_x0_N4;  break;
        case 5:  family = tau_x0_N5;  break;
        case 6:  family = tau_x0_N6;  break;
        case 7:  family = tau_x0_N7;  break;
        case 8:  family = tau_x0_N8;  break;
        case 9:  family = tau_x0_N9;  break;
        case 10: family = tau_x0_N10; break;
        case 11: family = tau_x0_N11; break;
        case 12: family = tau_x0_N12; break;
        case 13: family = tau_x0_N13; break;
        case 14: family = tau_x0_N14; break;
        case 15: family = tau_x0_N15; break;
        default: family = tau_x0_N1;  break;
    }

        // ---- Extract columns ----
        int n = family.length;
        double[] tau  = new double[n];
        double[] xVal = new double[n];
        double[] zVal = new double[n];
        double[] ydVal= new double[n];

        for (int i = 0; i < n; i++) {
            tau[i]  = family[i][0];
            xVal[i] = family[i][1];
            zVal[i] = family[i][2];
            ydVal[i]= family[i][3];
        }

        // ---- Clamp tau0 to valid range ----
        tau0 = Math.max(tau[0], Math.min(tau0, tau[n - 1]));

        // ---- Build spline interpolators ----
       CubicSpline sx  = new CubicSpline(tau, xVal);
       CubicSpline sz  = new CubicSpline(tau, zVal);
       CubicSpline syd = new CubicSpline(tau, ydVal);

       // ---- Evaluate at (possibly clamped) tau0 ----
       double x    = sx.value(tau0);
       double z    = sz.value(tau0);
       double ydot = syd.value(tau0);

        // Mirror if pm = -1
        if (pm < 0) {
            z    = -z;
        }

        return new double[] {tau0, x, 0.0, z, 0.0, ydot, 0.0, muStar, tStar, lStar };
    }

