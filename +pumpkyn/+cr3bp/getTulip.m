function [tau0,x0,mu,tStar,lStar] = getTulip(tau0,Np,pm,tol)
%% Purpose:
%
%  This routine will fetch previous cataloged initial states for all 
%  previousl known tulip-shaped orbits in the cr3bp system. It will then
%  use a continuation method to ensure that they maintain proper closure
%  for the user specified period.
%
%% Inputs:
%
%  tau0                 double                  Dimensionless Period of
%                                               desired tulip-shaped orbit
%
%  Np                   integer                 Number of Petals associated
%                                               with the tulip-shaped
%                                               orbit.
%
%  pm                   integer                 +1 = northern hemisphere
%                                               -1 = southern hemisphere
%
%  tol                  double                  Tolerance used for
%                                               natural parameter cont
%                                               (NaN = no cont)
%
%% Outputs:
%
%  tau0                 [N x 1]                 Dimensionless Period
%                                               corresponding to the 
%                                               returned state of the
%                                               tulip-shaped orbit
%
%  x0                   [1 x 6]                 Full Dimensionless States
%                                               of the Tulip-shaped orbit
%                                               corresponding to
%                                               [x,y,z,xdot,ydot,zdot]
%
%% Revision History:
%  Darin C. Koblick                                              08/26/2025
%  Copyright 2025 Coorbital, Inc.
%% --------------------------- Begin Code Sequence ------------------------
if nargin == 0
          Np = 14;
        tau0 = 2*pi;
          pm = +1;
   %Get the state:       
   [tau0,x0,mu] = pumpkyn.cr3bp.getTulip(tau0,Np,pm);
             x0 = pumpkyn.cr3bp.cont_np(x0,tau0,mu,1e-12);
         [~,x] = pumpkyn.cr3bp.prop([0 tau0],x0,mu);
        
     figure('color',[1 1 1]);
     plot3(x(:,1),x(:,2),x(:,3),'-r'); hold on;
     axis equal;
   return;
end

if ~exist("tol","var")
    tol = NaN;
end

%% Three-body constants:
        mu = 0.012150585609624;
     lStar = 389703.264829278;
     tStar = 382981.289129055;

%% Lookup Orbit from Seed States:
    tau_x0 = createLookupTable();

if isempty(tau0)
      tau0 = tau_x0{Np}(:,1);
end

%% Ensure states are within bounds    
    if any(tau_x0{Np}(end,1) < tau0)
      %Return the closest tau0 value
        tau0(tau_x0{Np}(end,1) < tau0) = tau_x0{Np}(end,1);
    elseif any(tau_x0{Np}(1,1) > tau0)
        tau0(tau_x0{Np}(1,1) > tau0) = tau_x0{Np}(1,1);
    end
    
    tau0 = unique(tau0);
    
%% Perform spline interpolation on states:
      x0 = interp1(tau_x0{Np}(:,1),tau_x0{Np}(:,2:4),tau0,'spline');
      x0 = [x0(:,1),zeros(size(x0,1),1),pm.*x0(:,2), ...
            zeros(size(x0,1),1),x0(:,3),zeros(size(x0,1),1)];

%% Clean up w/ Natural Parameter Cont w/ fixed period:
if ~isnan(tol)
      for ts=1:numel(tau0)
        x0(ts,:) = pumpkyn.cr3bp.cont_np(x0(ts,:),tau0(ts),mu,tol);
      end
end
      
end

